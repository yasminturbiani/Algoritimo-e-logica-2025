print("Vamos calcular a sua média")

nota_trabalho = float(input("Qual a nota do seu trabalho? "))
nota_seminario = float(input("Qual a nota do seu seminário? "))
nota_prova = float(input("Qual a nota da sua prova? "))
nota_artigo = float(input("Qual a nota do seu artigo? "))

media = (nota_trabalho*0.2) + (nota_seminario*0.3) + (nota_prova*0.3) + (nota_artigo*0.2)

print (f" A sua média final é: {media}")

